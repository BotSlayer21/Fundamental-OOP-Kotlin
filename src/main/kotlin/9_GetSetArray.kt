//9 Latihan Data Types (Array -> Get and set the elements of an array)
fun main() {
    val kota = arrayOf<String>("Jakarta", "Surabaya", "Bandung", "Yogyakarta")
    println(kota[0])
    println(kota.get(1))
    println("-----------------")

    kota[0] = "Jakarta Pusat"
    kota.set(1, "Surabaya Timur")
    println(kota[0])
    println(kota.get(1))
    println(kota.get(2))
}
